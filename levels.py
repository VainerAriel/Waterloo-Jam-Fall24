def load_level(level):
    tiles = []
    movable_tiles = []
    player = None
    # player = Player(...)

    for y in range(len(level)):
        for x in range(len(level[y])):
            tile_type = level[y][x]
            tile = Tile(screen, (x, y), (0, 0, 0))
            if tile_type == 1:
                tile = Tile(screen, (x, y), (100, 100, 100), collidable=True)
            elif tile_type == 2:
                player = Player(screen, (x, y), (0, 255, 0), images=idle_anim[0])
            elif tile_type == 3:
                tiles.append(Tile(screen, (x, y), (0, 0, 0)))
                tile = Tile(screen, (x, y), (0, 0, 255), collidable=True, movable=True)
                movable_tiles.append(tile)
            # do some ifs with different file_types, including movable tiles/blocks
            tiles.append(tile)

    return tiles, movable_tiles, player
