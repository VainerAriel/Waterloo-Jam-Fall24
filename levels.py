    player = None
    # player = Player(...)

    for y in range(len(level)):
        for x in range(len(level[y])):
            tile_type = level[y][x]
            tile = Tile(screen, (x, y), (255, 255, 255))
            if tile_type == 1:
                tile = Tile(screen, (x, y), (100, 100, 100), collidable=True)
            elif tile_type == 2:
                player = Player(screen, (x, y), (0, 255, 0))
